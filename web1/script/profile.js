function openNav() {
    document.getElementById("mySidenav").style.width = "350px";
    document.getElementById("main").style.marginLeft = "350px";
    document.getElementById("maiH").style.marginLeft = "350px";
    document.getElementById("maiH").style.fontSize="30px";
    document.getElementById("topnav").style.marginLeft = "350px";
    document.getElementsByClassName("man").style.marginLeft = "350px";
    document.body.style.backgroundColor ="black";
  }
  
  function closeNav() {
    document.getElementById("mySidenav").style.width = "0";
    document.getElementById("main").style.marginLeft= "0";
    document.getElementById("maiH").style.marginLeft= "0";
    document.getElementById("maiH").style.fontSize="70px";
    document.getElementById("topnav").style.marginLeft= "0"
    document.getElementsByClassName("man").style.marginLeft= "0";
    document.body.style.backgroundColor = "black";
  }
  
  
